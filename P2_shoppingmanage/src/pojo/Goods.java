public class Goods {
    private int gid;
    private String gname;
    private int gnum;
    private String gmade;
    private double gprice;
    private double gbalance;
    private String gpic;
    private int tid;

    public int getGid() {
        return gid;
    }

    public void setGid(int gid) {
        this.gid = gid;
    }

    public String getGname() {
        return gname;
    }

    public void setGname(String gname) {
        this.gname = gname;
    }

    public int getGnum() {
        return gnum;
    }

    public void setGnum(int gnum) {
        this.gnum = gnum;
    }

    public String getGmade() {
        return gmade;
    }

    public void setGmade(String gmade) {
        this.gmade = gmade;
    }

    public double getGprice() {
        return gprice;
    }

    public void setGprice(double gprice) {
        this.gprice = gprice;
    }

    public double getGbalance() {
        return gbalance;
    }

    public void setGbalance(double gbalance) {
        this.gbalance = gbalance;
    }

    public String getGpic() {
        return gpic;
    }

    public void setGpic(String gpic) {
        this.gpic = gpic;
    }

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }
}
